// Language management
let currentLanguage = localStorage.getItem('bryssel-lang') || 'sv';

// Get translation by key
function t(key) {
    return translations[currentLanguage][key] || translations['sv'][key] || key;
}

// Update all translatable elements on the page
function updatePageLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('bryssel-lang', lang);
    
    // Update HTML lang attribute
    document.documentElement.lang = lang === 'sv' ? 'sv' : 'en';
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        // Check if it's an input with placeholder
        if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update elements with data-i18n-html (for HTML content)
    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        const key = element.getAttribute('data-i18n-html');
        element.innerHTML = t(key);
    });
    
    // Update active state on language switcher
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    
    // Dispatch custom event for other scripts to listen to
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language
    updatePageLanguage(currentLanguage);
    
    // Add click handlers to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            updatePageLanguage(lang);
        });
    });
});

